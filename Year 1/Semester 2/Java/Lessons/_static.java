package classes;

public class _static {

    // static is unable to be accessed without calling it on an instance.
    // Its like a function from other languages

    public static void this_is_static()  {
        System.out.println("Hello");
    }

    public void this_is_not_static() {
        System.out.println("Hello");

    }
    // just a reminder that void does not return anything
}
    

